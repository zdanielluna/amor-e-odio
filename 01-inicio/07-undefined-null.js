/* Undefined
    Valor primitivo, utilizado quando uma variável 
    não teve valor atribuído. Indica a ausência de um valor qualquer. */

/* Null 
    Valor primitivo que representa a ausência intencional de 
    um valor de objeto, não aponta para nenhum endereço de memória. */

// Valor igual
console.log(null == undefined)

// Tipo diferente
console.log(null === undefined)

console.log(typeof null)
console.log(typeof undefined)
