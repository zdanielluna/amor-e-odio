// São utilizados para determinar a igualdade ou diferença entre variaveis ou valores

// Maior que
console.log('18' > 18)

// Maior ou igual
console.log('18' >= 18)

// Menor que
console.log('18' < 18)

// Menor ou igual
console.log('18' <= 18)

// Verifica se são iguais no valor
console.log('18' == 18)

// Verifica se são iguais no valor e no tipo
console.log('18' === 18)

// Verifica se são diferentes no valor
console.log('18' != 18)

// Verifica se são diferentes no valor ou no tipo
console.log('18' !== 18)


