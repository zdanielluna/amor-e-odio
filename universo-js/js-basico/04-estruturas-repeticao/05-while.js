/* Pode ser que não ocorra nenhuma repetição se a expressão lógica do while
   iniciar com o valor false.
   
   A repetição ocorre até que a condição de parada seja ativada, ou seja, quando
   a expressão lógica do while for falsa. */

while(false) {
  console.log('Esta mensagem não vai ser exibida!')
}

while (true) {
  console.log('Eternal Genjutsu')
}

